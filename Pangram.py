sent = input('Enter a sentence: ')

letter = {
    "a" : 0,
    "b" : 0,
    "c" : 0,
    "d" : 0,
    "e" : 0,
    "f" : 0,
    "g" : 0,
    "h" : 0,
    "i" : 0,
    "j" : 0,
    "k" : 0,
    "l" : 0,
    "m" : 0,
    "n" : 0,
    "o" : 0,
    "p" : 0,
    "q" : 0,
    "r" : 0,
    "s" : 0,
    "t" : 0,
    "u" : 0,
    "v" : 0,
    "w" : 0,
    "x" : 0,
    "y" : 0,
    "z" : 0
}

panagram = True

for alphabet in sent:
    if alphabet in letter:
        letter[alphabet] += 1

for item in letter:
    if letter[item] == 0:
        panagram = False

if panagram == True:
    print('The sentence is a pangram')

else:
    print('The sentence is not a pangram')
